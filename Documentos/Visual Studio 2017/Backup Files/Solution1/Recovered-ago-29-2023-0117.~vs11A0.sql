   AND FCFOCOMPL.STSDOCUMENTACAO <> ''  
   OR FCFOCOMPL.STSLICENCAAMBIENTAL <> ''
   OR FCFOCOMPL.DATAVALIDADE <> ''
   OR FCFOCOMPL.STSPOLITICASOCIOAMBI <> */